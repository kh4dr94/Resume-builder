// Resolve language code to a supported locale
function resolveLangCode(lang) {
  // Map English variants to en-US
  if (lang?.startsWith('en-')) return 'en-US'
  if (translations[lang]) return lang
  return 'en-US'
}

// Helper to get nested keys like "form.fullName"
export function t(lang, key) {
  const langCode = resolveLangCode(lang)
  const keys = key.split('.')
  let value = translations[langCode]
  for (const k of keys) {
    value = value?.[k]
  }
  if (value === undefined) {
    // Fallback to English
    value = translations['en-US']
    for (const k of keys) {
      value = value?.[k]
    }
  }
  return value || key
}
